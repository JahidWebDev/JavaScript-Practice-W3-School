
// JavaScript Assignment

// operator             Example            Same As
//   =                   x = y              x = y

// +=                    x += y             x = x + y 

// -=                    x -= y             x = x - y

// *=                    x *= y             x = x * y

// /=                    x /= y             x = x / y

// %=                    x %= y             x = x % y

// **=                   x **= y            x = x ** y


// The += Operator
// The Addition Assignment Operator adds a value to a variable.

// let x = 10;
// x += 5; 
// output: 15

// The -= Operator
// Subtraction Assignment Example

// let x = 10;
// x -= 5; 
// output: 5


// The *= Operator
// Multiplication Assignment Example

// let x = 10;
// x *= 5; 
// output: 2